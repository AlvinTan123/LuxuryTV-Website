                     The Samsung 98" NEO QLED 4K QN90A also has built-in Bixby and Alexa voice assistants, access to major streaming services through Samsung's Smart TV platform, and a variety of connectivity choices like Wi-Fi, Bluetooth, and HDMI. In general, the QN90A is a top-tier TV that provides a completely immersive watching experience.
